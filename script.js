// ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ó‡∏≥‡∏Ç‡πâ‡∏≠‡∏™‡∏≠‡∏ö
function startQuiz() {
    currentQuestionIndex = 0;
    score = 0;
    correctAnswers = 0;
    timeLeft = quiz.timeLimit;
    userAnswers = [];
    nextButton.disabled = true;
    nextButton.style.display = "block";
    restartButton.style.display = "none";
    resultContainer.style.display = "none";
    answerSheet.innerHTML = "";
    timerDisplay.textContent = timeLeft;
    loadQuestion();
    startTimer();
}

// ‡πÇ‡∏´‡∏•‡∏î‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°
function loadQuestion() {
    const question = quiz.questions[currentQuestionIndex];
    questionText.textContent = question.text;
    choicesContainer.innerHTML = "";

    question.choices.forEach(choice => {
        const button = document.createElement("button");
        button.textContent = choice;
        button.addEventListener("click", () => selectAnswer(button, choice, question.correct));
        choicesContainer.appendChild(button);
    });

    nextButton.disabled = true;
}

// ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö
function selectAnswer(button, selected, correct) {
    let buttons = choicesContainer.querySelectorAll("button");

    // ‡πÄ‡∏Ñ‡∏•‡∏µ‡∏¢‡∏£‡πå‡∏™‡∏µ‡∏ó‡∏µ‡πà‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏Å‡πà‡∏≠‡∏ô‡∏´‡∏ô‡πâ‡∏≤
    buttons.forEach(btn => btn.classList.remove("selected"));
    button.classList.add("selected");

    // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö
    userAnswers[currentQuestionIndex] = { 
        question: quiz.questions[currentQuestionIndex].text, 
        selected, 
        correct,
        isCorrect: selected === correct 
    };

    nextButton.disabled = false;
}

// ‡πÑ‡∏õ‡∏¢‡∏±‡∏á‡∏Ñ‡∏≥‡∏ñ‡∏≤‡∏°‡∏ñ‡∏±‡∏î‡πÑ‡∏õ
nextButton.addEventListener("click", () => {
    currentQuestionIndex++;
    if (currentQuestionIndex < quiz.questions.length) {
        loadQuestion();
    } else {
        endQuiz();
    }
});

// ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏à‡∏±‡∏ö‡πÄ‡∏ß‡∏•‡∏≤
function startTimer() {
    timer = setInterval(() => {
        timeLeft--;
        timerDisplay.textContent = timeLeft;

        if (timeLeft <= 0) {
            clearInterval(timer);
            endQuiz();
        }
    }, 1000);
}

// ‡∏à‡∏ö‡∏Ç‡πâ‡∏≠‡∏™‡∏≠‡∏ö‡πÅ‡∏•‡∏∞‡πÅ‡∏™‡∏î‡∏á‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô
function endQuiz() {
    clearInterval(timer);

    // ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡πÅ‡∏•‡∏∞‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏Ç‡πâ‡∏≠‡∏ó‡∏µ‡πà‡∏ï‡∏≠‡∏ö‡∏ñ‡∏π‡∏Å
    correctAnswers = userAnswers.filter(ans => ans.isCorrect).length;
    score = correctAnswers * 10; // ‡πÉ‡∏´‡πâ 10 ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏ï‡πà‡∏≠‡∏Ç‡πâ‡∏≠
    let percentage = (score / (quiz.questions.length * 10)) * 100;

    // ‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå
    scoreText.innerHTML = `üéØ ‡∏Ñ‡∏∏‡∏ì‡∏ï‡∏≠‡∏ö‡∏ñ‡∏π‡∏Å <b>${correctAnswers}/${quiz.questions.length}</b> ‡∏Ç‡πâ‡∏≠<br>üìä ‡∏Ñ‡∏∞‡πÅ‡∏ô‡∏ô‡∏ó‡∏µ‡πà‡πÑ‡∏î‡πâ: <b>${score}/${quiz.questions.length * 10} (${percentage}%)</b>`;
    statusText.textContent = percentage >= quiz.passingScore ? "üéâ ‡∏™‡∏≠‡∏ö‡∏ú‡πà‡∏≤‡∏ô!" : "‚ùå ‡∏™‡∏≠‡∏ö‡πÑ‡∏°‡πà‡∏ú‡πà‡∏≤‡∏ô!";

    resultContainer.style.display = "block";
    nextButton.style.display = "none";
    restartButton.style.display = "block";

    showAnswerSheet();
}

// ‡πÅ‡∏™‡∏î‡∏á‡πÄ‡∏â‡∏•‡∏¢‡∏Ç‡πâ‡∏≠‡∏™‡∏≠‡∏ö
